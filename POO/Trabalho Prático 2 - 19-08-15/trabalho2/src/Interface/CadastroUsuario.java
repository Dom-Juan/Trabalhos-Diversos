    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Controlador control = new Controlador();
        String nome,curso,ano,titulacao,codUsuario;
        int tipoAluno;//0 = aluno; 1 = professor
        int diasEmprestimos;
        
        codUsuario = jTextField1.getText();
        tipoAluno = jComboBox1.getSelectedIndex();
        nome = textFieldNome.getText();
        
        if(tipoAluno == 0){
            curso = textFieldCurso.getText();
            ano = textFieldAno.getText();
            diasEmprestimos = control.getConfig().getDiasAlunos();
            Aluno novoAluno = new Aluno(codUsuario, nome, diasEmprestimos  , curso, Integer.valueOf(ano));
            control.addUsuario(novoAluno);
            
        }else{
            titulacao = textFieldTitulacao.getText();
            diasEmprestimos = control.getConfig().getDiasAlunos();
            Professor novoProfessor = new Professor(codUsuario, nome, diasEmprestimos, titulacao);
            control.addUsuario(novoProfessor);
        }
        

        this.dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

