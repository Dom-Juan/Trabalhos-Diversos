    private void jComboBox1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jComboBox1FocusLost
        int indexComboBox = jComboBox1.getSelectedIndex();
        
        if(indexComboBox == 0){
            textFieldTitulacao.setEnabled(false);
            textFieldAno.setEnabled(true);
            textFieldCurso.setEnabled(true);
        }else{
            textFieldTitulacao.setEnabled(true);
            textFieldAno.setEnabled(false);
            textFieldCurso.setEnabled(false);
        }
    }//GEN-LAST:event_jComboBox1FocusLost

