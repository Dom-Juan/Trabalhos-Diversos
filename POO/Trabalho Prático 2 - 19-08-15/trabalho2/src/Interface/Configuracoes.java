    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //save

        String emp = empTextField.getText();
        String use = userTextField.getText();
        String boo = bookTextField.getText();
        String alun = jTextField1.getText();
        String prof = jTextField2.getText();
            
        if(alun.contains("-") || prof.contains("-")){
            JOptionPane.showMessageDialog(null, "Não pode ter número negativo na quantidade de dias de empréstimo !!");
            return;
        }
        
        if(emp.equals(use) || emp.equals(boo) || use.equals(boo)){
            JOptionPane.showMessageDialog(null, "Os Arquivos não podem ter nomes iguais");
            return;
        }
        
        config.setDiasAlunos(Integer.parseInt(jTextField1.getText()));
        config.setDiasProfessor(Integer.parseInt(jTextField2.getText()));
        config.setArquivoEmprestimos(empTextField.getText());
        config.setArquivoUsuarios(userTextField.getText());
        config.setArquivoLivros(bookTextField.getText());
        
        Controlador control = new Controlador();
        control.setConfig(config);
        this.dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

