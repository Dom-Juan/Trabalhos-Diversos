        jTextField2.setText(String.valueOf(this.config.getDiasProfessor()));
        empTextField.setText(this.config.getArquivoEmprestimos());
        userTextField.setText(this.config.getArquivoUsuarios());
        bookTextField.setText(this.config.getArquivoLivros());
