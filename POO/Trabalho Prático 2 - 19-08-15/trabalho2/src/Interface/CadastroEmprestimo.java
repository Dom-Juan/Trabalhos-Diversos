    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //Salvar o cliente cadastrado
        Controlador control = new Controlador();
                
        Emprestimo emp = new Emprestimo(String.valueOf(this.codEmprestimo), this.codLocal); 

        Item item;
        for(int i=0;i<local.size();i++){
            item = local.get(i);
            if(item.isDevolvido() == false){
                control.setTrueLivro(item.getLivro().getCodLivro());
            }
        }
           
        emp.setItens(this.local);
        
        control.addEmprestimo(emp);
        
        this.dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

