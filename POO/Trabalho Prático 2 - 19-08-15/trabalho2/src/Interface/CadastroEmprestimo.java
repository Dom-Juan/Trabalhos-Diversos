    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //botaosearch
        String cod;
        cod = codigoUsuario.getText();
        Controlador control = new Controlador();
        Usuario user;
        user = control.procurarUsuario(cod);
        if(user == null) {
            JOptionPane.showMessageDialog(null, "Usuário Não Cadastrado!!");
            return;
        }
        else nameField.setText(user.getNome());
        
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        for(int i=dtm.getRowCount()-1;i>=0;i--) dtm.removeRow(i);
        this.local.clear();//emprestimso atuais;
        this.codLocal = cod;//codigo atual do usuario;
      
        codLivro.setEnabled(true);
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String cod = codLivro.getText();
        Controlador control = new Controlador();
        Livro livro = control.procurarLivro(cod);
        
        if(livro == null){
            JOptionPane.showMessageDialog(null, "Livro Não Cadastrado !!");
        }
        else if(livro.isEmprestado() == true){
            JOptionPane.showMessageDialog(null, "Este Livro Já Está Emprestado !!");
        }
        else if(control.verificaItemValido(local, livro) == true){
            JOptionPane.showMessageDialog(null, "Este Livro Já Está Emprestado !!");
        }
        else{     
            int con = JOptionPane.showConfirmDialog(null, "Deseja Realmente Emprestar Este Livro:\nCódigo: "+livro.getCodLivro()+"\nNome: "+livro.getNome());
            if(con != 0){
                return;
            }
                    
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            String linha [] = new String[5];
            Calendar calemprestimo = Calendar.getInstance();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");           
            linha[0] = livro.getCodLivro();
            linha[1] = livro.getNome();
            linha[2] = String.valueOf(livro.getAno());
            linha[3] = sdf.format(calemprestimo.getTime());

            Usuario user = control.procurarUsuario(this.codLocal);
            int quant;
            if(user instanceof Aluno){
                quant = control.quantDiasAlunos();
            }
            else{
                quant = control.quantDiasProfessores();
            }
            
     
            Calendar caldevolucao = calemprestimo;
            caldevolucao.set(caldevolucao.get(Calendar.YEAR),caldevolucao.get(Calendar.MONTH),caldevolucao.get(Calendar.DAY_OF_MONTH)+quant);
            
            linha[4] = sdf.format(caldevolucao.getTime());
            
            Item iten = new Item(livro, calemprestimo, caldevolucao, String.valueOf(this.codEmprestimo));
            local.add(iten);
            
            dtm.addRow(linha);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

