    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Controlador control = new Controlador();
        String cod;
        cod = codUsuario.getText();
        Usuario user;
        user = control.procurarUsuario(cod);
        if(user == null){
            JOptionPane.showMessageDialog(null, "Usuário Não Encontrado !!");
            return;
        }
        
        nameField.setText(user.getNome());
       
        
        this.local = new ArrayList<Item>();
        
        this.codLocal = cod;
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        for(int i=dtm.getRowCount()-1;i>=0;i--) dtm.removeRow(i);
        this.removerei = new ArrayList<String[]>();
        
        
        
        local = control.getItensDeUmUsuario(cod);
        if(local == null){
            JOptionPane.showMessageDialog(null, "O usuário não possui livros emprestados!!");
            return;
        }
        Item item;
        String linha[] = new String[5];
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        for(int i=0;i<local.size();i++){
            item = local.get(i);
            linha[0] = item.getCodEmprestimo();
            linha[1] = item.getLivro().getCodLivro();
            linha[2] = item.getLivro().getNome();
            linha[3] = sdf.format(item.getDataEmprestimo().getTime());
            linha[4] = sdf.format(item.getDataDevolucao().getTime());
            
            dtm.addRow(linha);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

