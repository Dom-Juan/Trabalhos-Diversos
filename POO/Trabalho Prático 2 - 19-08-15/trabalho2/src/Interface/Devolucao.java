    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //remover selecionados;
        int verif=0;
        if(jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Não Existem Livros Na Tabela !!");
            return;
        }
        
        String codEmprestimo;
        String codLivro;
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<Integer> deletaLinha = new ArrayList<Integer>();
        for(int i = 0;i<jTable1.getRowCount();i++){
            if(jTable1.getValueAt(i, 5) != null && (boolean)jTable1.getValueAt(i, 5) == true){
                codEmprestimo = (String) jTable1.getValueAt(i, 0);
                codLivro = (String) jTable1.getValueAt(i, 1);
                deletaLinha.add(i);
                String linha []= {codEmprestimo, codLivro};
                removerei.add(linha);
            }
        }
        if(deletaLinha.size() ==0){
            JOptionPane.showMessageDialog(null, "Não Existem Itens Selecionados Na Tabela");
            return;
        }
        verif = JOptionPane.showConfirmDialog(null, "Deseja Realmente Devolver Todos Os Itens Selecionados?");
        if(verif != 0) return;
        
        for(int i=deletaLinha.size()-1;i>=0;i--){
            dtm.removeRow(deletaLinha.get(i));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

