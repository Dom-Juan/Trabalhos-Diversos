    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selected = jTable1.getSelectedRow();
        if(selected == -1)return;
        else{
            Venda[] vendas;
            ControladorVendas control = new ControladorVendas();
            try {
                vendas = control.getVendas();
                if(vendas == null)return;
                
                
                
                TabelaItens table = new TabelaItens((Frame) this.getParent(),true,vendas[Integer.parseInt((String) jTable1.getValueAt(selected, 0))]);
                table.setLocationRelativeTo(this);
                table.toFront();
                table.setVisible(true);
                
            } catch (IOException | ClassNotFoundException ex) {
                Logger.getLogger(VPTDP_Completo.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

