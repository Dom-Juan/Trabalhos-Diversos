    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Verifica qual tipo de pagamento o usuário solicitou e exibe os dados relativos as vendas desse tipo
        
        int select = ComboBox.getSelectedIndex();
        ControladorVendas control = new ControladorVendas();
        
        for(int i = dTM.getRowCount()-1;i>=0;i--){
            dTM.removeRow(i);
        }
        
        try {
            Venda vendas[] = control.getVendas();
            if(vendas == null){
                JOptionPane.showMessageDialog(null,"Não Existem Vendas Cadastradas!");
                return;
            }
            
            switch(select){//Switch para exibir os dados de acordo com a entrada do usuario
                case 0:{
                    //dinheiro;
                    for(int i =0;i<vendas.length;i++){
                        if(vendas[i].getTipoPgto() instanceof Dinheiro){
                            this.addDadoNaTabela(vendas[i], 1);
                        }
                    }
                    String coluna [] = {"Número Da Nota","Nome do Cliente", "CPF do Cliente", "Data da Compra", "", ""};
                    dTM.setColumnIdentifiers(coluna);
                    break;
                }
                case 1:{
                    //cartão;
                    for(int i =0;i<vendas.length;i++){
                        if(vendas[i].getTipoPgto() instanceof Cartao){
                            this.addDadoNaTabela(vendas[i], 2);
                        }
                    }
                    String coluna [] = {"Número Da Nota","Nome do Cliente", "CPF do Cliente", "Data da Compra", "Titular do Cartão", "Número do Cartão"};
                    dTM.setColumnIdentifiers(coluna);
                    break;
                }
                case 2:{
                    //cheque;
                    for(int i =0;i<vendas.length;i++){
                        if(vendas[i].getTipoPgto() instanceof Cheque){
                            this.addDadoNaTabela(vendas[i], 3);
                        }
                    }
                    String coluna [] = {"Número Da Nota","Nome do Cliente", "CPF do Cliente", "Data da Compra", "Nome do Emissor", "Número do Cheque"};
                    dTM.setColumnIdentifiers(coluna);
                    break;
                }
            }//end switch
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(VPTDP_Simples.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

