    private void addDadoNaTabela(Venda venda, int tipo) {
        //Adcionada os dados da venda na tabela
        //O segundo parâmetro determina qual o tipo de pagamento da venda
        
        Object linha [] = new String[6];
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Calendar cal;
        cal = venda.getData();

        linha[0] = venda.getNumero();
        linha[1] = venda.getCliente().getNome();
        linha[2] = venda.getCliente().getCpf();
        linha[3] = sdf.format(cal.getTime());
        
        
        switch(tipo){
            case 1:{
                linha[4] = "";
                linha[5] = "";
                break;
            }
            case 2:{
                //cartão
                Cartao c = (Cartao) venda.getTipoPgto();
                linha[4] = c.getNome();
                linha[5] = c.getNumero();
                break;
            }
            case 3:{
                Cheque c = (Cheque) venda.getTipoPgto();
                linha[4] = c.getNomeEmissor();
                linha[5] = c.getNumeroCheque();
                break;
            }
        }//end switch
        dTM.addRow(linha);
    }
    
