    private void btnProcurarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarClienteActionPerformed
        //Procura um cliente de acordo com o CPF
        
        String cpf;
        Cliente c;
        ControladorClientes control = new ControladorClientes();
        
        cpf = textFieldCPF.getText();
        if(cpf.isEmpty()){
            JOptionPane.showMessageDialog(null, "Por Favor Introduza o CPF do Cliente");
            return;
        }
        try {
            c = control.buscarCliente(cpf);
            if(c == null){
                JOptionPane.showMessageDialog(null, "Cliente NÃ£o Encontrado!!");
            }
            else{
                textFieldNome.setText(c.getNome());
            }
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(BuscarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnProcurarClienteActionPerformed

