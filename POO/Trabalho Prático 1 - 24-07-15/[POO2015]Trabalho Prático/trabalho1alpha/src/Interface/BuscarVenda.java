    private void btnProcurarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarVendaActionPerformed
        //Procura uma venda de acordo com o número
        
        String numVenda;
        Venda vendaBuscada;
        ControladorVendas control = new ControladorVendas();
        
        numVenda = textFieldNumVenda.getText();
        if(numVenda.isEmpty()){
            JOptionPane.showMessageDialog(null, "Numero da venda esta vazio");
            return;
        }
        try {
            vendaBuscada = control.buscaVendaNum(numVenda);
            if(vendaBuscada == null){
                JOptionPane.showMessageDialog(null,"Não foi encontrada nenhuma venda com essa numero");
                return;
            }else{
                
                ExibirDadosVendas(vendaBuscada);
                
            }
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(BuscarVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnProcurarVendaActionPerformed

