    private void ExibirDadosVendas(Venda vendaBuscada) {
        //Exibe os dados da venda na tabela e nos textFields
        
        String informacao[] = {"","","",""};
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Calendar cal;
        DefaultTableModel dTM = new DefaultTableModel();
        Item item;
        Produto prdto;
        float total = 0;
        cal = vendaBuscada.getData();
        
        
        
        if(vendaBuscada.getTipoPgto() instanceof Dinheiro)textFieldTipoPgto.setText("Dinheiro");
        if(vendaBuscada.getTipoPgto() instanceof Cheque)textFieldTipoPgto.setText("Cheque");
        else textFieldTipoPgto.setText("Cartão");
        
        textFieldData.setText( sdf.format(cal.getTime()) );
        textFieldNomeCliente.setText( vendaBuscada.getCliente().getNome() );
        textFieldCPF.setText( vendaBuscada.getCliente().getCpf());
        
         dTM = (DefaultTableModel) tableItens.getModel();
         
        for(int i = dTM.getRowCount()-1 ;i>=0;i-- )dTM.removeRow(i);//Remove os itens da tabela caso haja algum
        
        for(int i =0;i< vendaBuscada.getContItens();i++){//Insere os itens da venda atual na tabela
             
             item = vendaBuscada.getItem(i);
             prdto = item.getPrdto();
           
             informacao[0] = prdto.getCodigo();
             informacao[1] = prdto.getDescricao();
             informacao[2] = String.valueOf( item.getQuantidade() );
             informacao[3] = String.valueOf( prdto.calcularPreco() );
             
             dTM.addRow(informacao);
             total += item.getQuantidade()*prdto.calcularPreco();
          
         }
        textFieldTotalVenda.setText(String.valueOf( total));
        
    }
